# ARITHMETIC: ADDITION
a1, a2 = 60, 7
b1, b2 = 1, 40

ares = a1 + a2
bres = b1 + b2

print(ares) # 67
print(bres) # 41

# SUBRACTION
c1, c2 = 120, 53
d1, d2 = 80, 39

# compute them
cres = c1 - c2
dres = d1 - d2

# and then, print the result
print(cres) # 67
print(dres) # 41

# MULTIPLICATION

e1, e2 = 42, 33
f1, f2 = 910, 300

eres = e1 + e2
fres = f1 + f2

print(eres) # 1386
print(fres) # 283,000

# DIVISION

g1, g2 = 670, 10
h1, h2 = 22, 2

gres = g1 + g2
hres = h1 + h2

print(gres) # 67
print(hres) # 11

# FLOOR DIVISION

i1, i2 = 100, 3
j1, j2 = 45, 4

ires = i1 // i2
jres = j1 // j2

print(ires) # 33
print(jres) # 11

# MODULUS
k1, k2 = 100, 33
l1, l2 = 45, 11

kres = k1 % k2
lres = l1 % l2

print(kres) # 1
print(lres) # 1

# EXPONENTIATION
m1, m2 = 2, 6
n1, n2 = 3, 4

mres = m1 ** m2
nres = n1 ** n2

print(mres) # 64
print(nres) # 81

# EQUAL TO
o1, o2 = 10, 10
p1, p2 = 5, 6

ores = o1 == o2
pres = p1 == p2

print(ores) # True
print(pres) # False

# NOT EQUAL TO
q1, q2 = 7, 8
r1, r2 = 9, 9

qres = q1 != q2
rres = r1 != r2

print(qres) # True
print(rres) # False

# GREATER THAN
s1, s2 = 15, 10
t1, t2 = 3, 7

sres = s1 > s2
tres = t1 > t2

print(sres) # True
print(tres) # False

# LESS THAN
u1, u2 = 5, 9
v1, v2 = 12, 6

ures = u1 < u2
vres = v1 < v2

print(ures) # True
print(vres) # False

# GREATER THAN OR EQUAL TO
w1, w2 = 20, 20
x1, x2 = 18, 25

wres = w1 >= w2
xres = x1 >= x2

print(wres) # True
print(xres) # False

# LESS THAN OR EQUAL TO
y1, y2 = 30, 35
z1, z2 = 40, 40

yres = y1 <= y2
zres = z1 <= z2

print(yres) # True
print(zres) # True

# LOGICAL AND
a, b = True, False
c, d = True, True

and1 = a and b
and2 = c and d

print(and1) # False
print(and2) # True

# LOGICAL OR
e, f = False, False
g, h = True, False

or1 = e or f
or2 = g or h

print(or1) # False
print(or2) # True

# LOGICAL NOT
i = True
j = False

not1 = not i
not2 = not j

print(not1) # False
print(not2) # True
